# Comparison Operators

10 > 3  # is greater than
10 >= 3  # is greater that or equal to'
10 < 20  # is less than
10 == 10  # 'is equal to'
10 == "10"  # False ("10" is a string, 10 is an integer)
10 != "10"  # True

"bag" > "apple"  # True
"bag" == "BAG"  # False
ord("b")  # <-- 98
ord("B")  # <-- 66
