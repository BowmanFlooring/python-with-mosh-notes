x = 1 + 2j  # a + bi
print(10 + 3)  # 13 (addition)
print(10 - 3)  # 7 (subtraction)
print(10 * 3)  # 30 (multiplication)
print(10 / 3)  # 3.333333333335 (division)
print(10 // 3)  # 3 (drops decimal)
print(10 % 3)  # 1 (remainder of a division)
print(10 ** 3)  # 1000 (exponent)

x = 10
# This can be simplified with an 'augmented assignment operator', like:
x = x + 3
# BOTH statements are exactly the same; this one is much simpler & shorter.
x += 3

print(round(2.9))  # 3
print(abs(-2.9))  # 2.9 (absolute value)

