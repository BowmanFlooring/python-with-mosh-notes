# Variables

# Variables are objects that are present in all
# programming languages in some way or another, and they
# simply store a defined value.

# 1. Variable names should be clear and descriptive!
# 2. All letters in variables should remain lower case.
# 3. In variables with multiple names, separate with an underscore.
# 4. Always put space between the "=" operator!

students_count = 1000  # integer
rating = 4.88  # floating point num.
